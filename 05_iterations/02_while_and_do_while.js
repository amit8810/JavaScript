// while Syntax
let index = 0
while(index < 10){
    // console.log(`value of index is ${index}`)
    index++
}

let hero = ['flash','superman','spiderman']
let idx = 0
while(idx < hero.length){
    // console.log(`${hero[idx]}`)
    idx++;
}

//-------------------------------------do while syntax
let score = 1
do{
    console.log(`score : ${score}`)
    score++
}while(score <= 10)
